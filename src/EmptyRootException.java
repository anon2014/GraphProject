
public class EmptyRootException extends Exception {

	public EmptyRootException() {
	}

	public EmptyRootException(String message) {
		super(message);
	}

	public EmptyRootException(Throwable cause) {
		super(cause);
	}

	public EmptyRootException(String message, Throwable cause) {
		super(message, cause);
	}

}
