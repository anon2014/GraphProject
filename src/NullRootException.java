
public class NullRootException extends Exception {

	public NullRootException() {
	}

	public NullRootException(String message) {
		super(message);
	}

	public NullRootException(Throwable cause) {
		super(cause);
	}

	public NullRootException(String message, Throwable cause) {
		super(message, cause);
	}

}
